num_semana = int(input("Digite o número da semana: "))

if num_semana == 1:
    print("Domingo")
elif num_semana == 2:
    print("Segunda-feira")
elif num_semana == 3:
    print("Terça-feira")
elif num_semana == 4:
    print("Quarta-feira")
elif num_semana == 5:
    print("Quinta-feira")
elif num_semana == 6:
    print("Sexta-feira")
elif num_semana == 7:
    print("Sábado")
else:
    print("Número inválido!")


